public class Demon {
    private String name;
    private int age;
    private String className;
    private String address;

    public String getName() {
        return name;
    }

    public Demon setName(String name) {
        this.name = name;
        return this;
    }

    public int getAge() {
        return age;
    }

    public Demon setAge(int age) {
        this.age = age;
        return this;
    }

    public String getClassName() {
        return className;
    }

    public Demon setClassName(String className) {
        this.className = className;
        return this;
    }

    public String getAddress() {
        return address;
    }

    public Demon setAddress(String address) {
        this.address = address;
        return this;
    }
}
